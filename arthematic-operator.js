var num1 = 20;
var num2 = 5;
var num3 = 7;

// + addition operator
var additionResult = num1 + num2 + num3;

console.log("My addition result is ",additionResult);

// - subtraction operator
var subtractionResult = num1 - num2 - num3;
console.log("My subtraction result is ", subtractionResult);


// * multiplication operator
var productResult = num1 * num2 * num3;
console.log("My product result is ", productResult);

// / division operator
var divisionResult = num1/num2/num3;
console.log("My division result is ", divisionResult);

// % modulus operator
var modulusResult = num1 % num3 % num2;
console.log("My modulus result is ",modulusResult);

// ** power operator
var powerResult = num1 ** num2;
console.log("Power result is ", powerResult);

// 2^3 => 2 * 2 * 2 => 8, 2**3
// 4^2 => 4 * 4 => 16, 4**2